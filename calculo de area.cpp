//calcular area = pi * raio²
#include <stdio.h> 
#include <math.h>
#define PI 3.13159
int main(){
	float raio, area;
	
	printf("Digite o valor do raio\n");
	scanf("%f", &raio); //raio é um dado de entrada
	
	area= PI * pow(raio,2); //area é um dado de saída
	
	printf("Area: %f", area);
	
}
